export const calculateDaysLeft = (targetDate) => {
  // Parse the target date string to a Date object
  const targetDateObj = new Date(targetDate);

  // Get the current date
  const currentDateObj = new Date();

  // Calculate the difference in milliseconds between the target date and the current date
  const timeDiff = targetDateObj.getTime() - currentDateObj.getTime();

  // Calculate the number of days left by dividing the time difference by the number of milliseconds in a day
  const daysLeft = Math.ceil(timeDiff / (1000 * 60 * 60 * 24));

  return daysLeft;
};

